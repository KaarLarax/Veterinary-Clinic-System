    private void b_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_ActualizarActionPerformed
        if(ct_Nombre.getText().equals("") ||
                ct_Telefono.getText().equals("") || 
                jdc_fContratacion.getDate() == null ||
                jdc_fNacimiento.getDate() == null ||
                cb_Especialidad.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {
            String name, telefono, especialidad;
            name = ct_Nombre.getText();
            telefono = ct_Telefono.getText();
            especialidad = cb_Especialidad.getSelectedItem().toString();
            
            Date fNacimiento = jdc_fNacimiento.getDate();
            Date fContratacion = jdc_fContratacion.getDate();
            if(fNacimiento == null || fNacimiento.before(jdc_fNacimiento.getMinSelectableDate()) || fNacimiento.after(jdc_fNacimiento.getMaxSelectableDate())) {
                JOptionPane.showMessageDialog(null, "Error en fecha", "ERROR", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            if(fContratacion == null || fContratacion.before(jdc_fContratacion.getMinSelectableDate()) || fContratacion.after(jdc_fContratacion.getMaxSelectableDate())) {
                JOptionPane.showMessageDialog(null, "Error en fecha", "ERROR", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            Calendar calInicio = Calendar.getInstance();
            calInicio.setTime(fNacimiento);

            Calendar calFin = Calendar.getInstance();
            calFin.setTime(fContratacion);
            
            int diferenciaAnios = calFin.get(Calendar.YEAR) - calInicio.get(Calendar.YEAR);
            
            if (calFin.get(Calendar.DAY_OF_YEAR) < calInicio.get(Calendar.DAY_OF_YEAR)) {
                diferenciaAnios--;
            }
            if(diferenciaAnios < 21 || diferenciaAnios > 80) {
                JOptionPane.showMessageDialog(null, "La fecha de contratación debe ser 21 años posterior a la de nacimiento y menor a 80", "ERROR", JOptionPane.ERROR_MESSAGE);
                calInicio.add(Calendar.YEAR, 21);
                jdc_fContratacion.setMinSelectableDate(calInicio.getTime());
                return;
            }
            int id = Integer.parseInt(ct_idVeterinario.getText());
            int res = VDB.actualizarRegistro(name, especialidad, telefono, new java.sql.Date(fNacimiento.getTime()), new java.sql.Date(fContratacion.getTime()), id);
            if (res > 0) {
                this.limpiar();
                this.listar();
            }
            
        }
    }//GEN-LAST:event_b_ActualizarActionPerformed

