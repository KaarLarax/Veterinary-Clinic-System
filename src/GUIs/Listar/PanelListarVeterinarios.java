    private void b_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_EliminarActionPerformed
        int res = 0;
        int fila = jt_Veterinarios.getSelectedRow();
        if (fila < 0) {
            JOptionPane.showMessageDialog(null, "Selecciona un registro de la tabla",
                    "Eliminar registro", 1);
        } else {
            try {
                res = VDB.eliminarRegistro(Integer.parseInt(jt_Veterinarios.getValueAt(fila, 0).toString()));
            } catch (NumberFormatException ex) {
                Logger.getLogger(PanelListarVeterinarios.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (res > 0) {
                this.limpiar();
                this.listar();
            }
        }
    }//GEN-LAST:event_b_EliminarActionPerformed

